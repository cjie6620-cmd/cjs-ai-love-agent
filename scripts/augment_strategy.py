def update_frontmatter(text: str) -> str:
    """更新 frontmatter 的 updated_at 与 version。保守策略：只改值，不重排键。"""
    m = re.match(r"^---\n(.*?)\n---\n", text, re.S)
    if not m:
        return text
    fm = m.group(1)
    new_fm = fm
    # 更新 updated_at
    if re.search(r"^updated_at:", new_fm, re.M):
        new_fm = re.sub(r"^updated_at:.*$", "updated_at: 2026-04-22", new_fm, flags=re.M)
    else:
        new_fm += "\nupdated_at: 2026-04-22"
    # 小版本升级
    if re.search(r"^version:\s*1\.0\.0\s*$", new_fm, re.M):
        new_fm = re.sub(r"^version:.*$", "version: 1.1.0", new_fm, flags=re.M)
    return f"---\n{new_fm}\n---\n" + text[m.end():]


def main() -> None:
    """主入口：遍历 GOAL_MAP 中声明的文档并追加骨架。"""
    modified = 0
    for rel, goal in GOAL_MAP.items():
        path = ROOT / rel
        if not path.exists():
            print(f"[skip] 不存在: {rel}")
            continue
        text = path.read_text(encoding="utf-8")
        if MARK in text:
            print(f"[skip] 已补齐: {rel}")
            continue
        text = update_frontmatter(text)
        text = text.rstrip() + "\n" + TEMPLATE.format(goal=goal, mark=MARK) + "\n"
        path.write_text(text, encoding="utf-8")
        modified += 1
        print(f"[ok]   已补齐: {rel}")
    print(f"\n共处理 {modified} 个文件。")


if __name__ == "__main__":
    main()
